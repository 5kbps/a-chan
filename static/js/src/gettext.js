function _(text){
	if(typeof getText[getSetting('language')] != 'undefined'){
		if(typeof getText[getSetting('language')][text] != 'undefined'){
			return getText[getSetting('language')][text]
		}else{
			return text
		}
	}else{
		return text
	}
}
function translateUI(){
	var list = byClass('gt')
	CycleThrough(list,function(item){
		var elem = list[item]
		var tag  = elem.tagName.toLowerCase()
		if(['input'].indexOf(tag) != -1){
			elem.value = _(elem.value)
		}else{
			elem.textContent = _(elem.textContent)
		}
	})
}
